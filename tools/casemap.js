function create_reverse_map()
{
    // Build a list of duplicate upper case mappings.  ie:
    // 0131	0049	LATIN SMALL LETTER DOTLESS I	LATIN CAPITAL LETTER I
    var dup_map = {};
    var dup_list = [];
    for (var i=0; i<map_l2u.length; i++)
    {
        var e = map_l2u[i];
        if (dup_map[e.u])
        {
            dup_list.push(e);
        }
        dup_map[e.u] = true;
    }

    // Copy list, removing duplicates
    var map2 = map_l2u.filter(x=>dup_list.indexOf(x) < 0);

    // Sort by upper case
    map2.sort((a, b) => a.u - b.u)

    return map2;
}


function create_ranges(map, f, t)
{
    let ranges = [];
    for (var i=0; i<map.length; )
    {
        for (var l=0; i+l<map.length; l++)
        {
            if (map[i+l][f] != map[i][f] +l || map[i+l][t] != map[i][t] + l)
                break;
        }
    
        var r = 
        {
            start: map[i][f],
            length: l,
            offset: map[i][t] - map[i][f],
        };
    
        ranges.push(r);
    
        i+=l;
    }
    return ranges;
}

function dump_ranges(ranges)
{
    for (let i=0; i<ranges.length; i++)
    {
        console.log("0x" + ranges[i].start.toString(16).padStart(4, 0) + ", " + ranges[i].length + ", " + ranges[i].offset + ", ")
    }
}

